public class Ch2Ex1 {
    public static void main(String[] args) {

        double circleRadius; // Radius of the circle
        double circleArea;   // Area of the circle

        // Assign a value to the radius
        circleRadius = 20.0;

        // Compute the area
        circleArea = circleRadius * circleRadius * 3.14159;

        // Display the result
        System.out.println("The area for the circle with radius " + circleRadius + " is " + circleArea);
    }
}